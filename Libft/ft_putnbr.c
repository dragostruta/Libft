#include "libft.h"

void	ft_putnbr(int n)
{
	int sg;
	int p;

	sg = n % 10;
	n /= 10;
	if (sg < 0)
	{
		ft_putchar('-');
		n = -n;
		sg = -sg;
	}
	if (n != 0)
	{
		p = 1;
		while (p * 10 <= n)
			p = p * 10;
		while (p != 0)
		{
			ft_putchar('0' + (n / p % 10));
			p = p / 10;
		}
	}
	ft_putchar(sg + '0');
}
