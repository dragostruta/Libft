#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t				i;
	const unsigned char *first;
	const unsigned char *second;

	i = 0;
	first = s1;
	second = s2;
	if (n == 0)
		return (0);
	while (i < n)
	{
		if (first[i] != second[i])
			return (first[i] - second[i]);
		i++;
	}
	return (0);
}
